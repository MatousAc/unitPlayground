  <h2>Google Account Data</h2>
  <p>
    When you log in to UnitPlayground with your Google account, we only collect
    your email address to store your app settings and history so that it will be
    available later. We can also sometimes see our users' profile pictures. We
    do not access, use, or see any other data from your Google account.
  </p>

